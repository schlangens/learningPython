# Lists which are like arrays - can hold any type
# They are mutable, you can change whats in them

my_list = [1, 2, 3, 4, 5, 6]

# adding types to lists can be done like this

my_list = my_list + [7, 8]

# You can also use the append method like so This will add a single item to a list

my_list.append(9)

# another list method called extend. If you want to add multiple items.

my_list.extend([10, 11, 12])

#


print(my_list)

